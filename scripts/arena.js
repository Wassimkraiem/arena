class Arena {
  constructor() {
    this.size = 7;
    this.grid = this.createGrid();
    this.players = [];
  }

  createGrid() {
    const grid = [];
    for (let row = 0; row < this.size; row++) {
      grid[row] = [];
      for (let col = 0; col < this.size; col++) {
        grid[row][col] = {
          row: row,
          col: col,
          player: null,
          type: "empty",
        };
      }
    }
    return grid;
  }

  addPlayer(player, row, col) {
    if (this.isValidPosition(row, col) && !this.grid[row][col].player) {
      this.grid[row][col].player = player;
      player.position = { row, col };
      this.players.push(player);
      return true;
    }
    return false;
  }

  movePlayer(player, newRow, newCol) {
    console.log(
      `üîç Tentative de d√©placement: ${player.name} de (${player.position.row}, ${player.position.col}) vers (${newRow}, ${newCol})`
    );

    // V√©rification que ce n'est pas la m√™me position
    if (player.position.row === newRow && player.position.col === newCol) {
      console.log("‚ùå Impossible de se d√©placer vers sa propre position");
      return false;
    }

    // V√©rification de base
    if (
      !this.isValidPosition(newRow, newCol) ||
      this.grid[newRow][newCol].player
    ) {
      console.log("‚ùå Position invalide ou occup√©e");
      return false;
    }

    // V√©rification que le mouvement respecte les r√®gles du h√©ros
    if (!this.isValidMoveForPlayer(player, newRow, newCol)) {
      console.log(
        `‚ùå Mouvement invalide pour ${player.heroType}: ne respecte pas les r√®gles de d√©placement`
      );
      return false;
    }

    const oldPos = player.position;
    this.grid[oldPos.row][oldPos.col].player = null;

    this.grid[newRow][newCol].player = player;
    player.position = { row: newRow, col: newCol };

    console.log(
      `‚úÖ ${player.name} s'est d√©plac√© de (${oldPos.row}, ${oldPos.col}) vers (${newRow}, ${newCol})`
    );
    return true;
  }

  // Fonction pour obtenir la distance de d√©placement selon le type de h√©ros
  getDistanceDeplacement(heroType) {
    if (heroType === "ninja") return 2;
    return 1; // Chevalier et Sorcier
  }

  // M√©thode pour valider si un mouvement respecte les r√®gles du h√©ros
  isValidMoveForPlayer(player, newRow, newCol) {
    const { row, col } = player.position;
    const deltaRow = newRow - row;
    const deltaCol = newCol - col;
    const absDeltaRow = Math.abs(deltaRow);
    const absDeltaCol = Math.abs(deltaCol);

    console.log(
      `üîç Validation mouvement ${player.heroType}: deltaRow=${deltaRow}, deltaCol=${deltaCol}`
    );

    // R√àGLE ABSOLUE 1: Aucun mouvement en diagonale autoris√©
    if (deltaRow !== 0 && deltaCol !== 0) {
      console.log("‚ùå INTERDIT: Mouvement en diagonale d√©tect√©");
      return false;
    }

    // R√àGLE ABSOLUE 2: Le mouvement doit √™tre dans UNE SEULE direction cardinale
    if (deltaRow === 0 && deltaCol === 0) {
      console.log("‚ùå INTERDIT: Aucun mouvement d√©tect√©");
      return false;
    }

    // R√àGLE ABSOLUE 3: Le mouvement doit √™tre dans UNE SEULE direction
    if (absDeltaRow > 0 && absDeltaCol > 0) {
      console.log("‚ùå INTERDIT: Mouvement dans plusieurs directions");
      return false;
    }

    switch (player.heroType) {
      case "chevalier":
      case "sorcier":
        // Chevalier et Sorcier: exactement 1 case dans UNE direction cardinale
        const isValidOneStepMove =
          (absDeltaRow === 1 && deltaCol === 0) || // 1 case verticalement
          (deltaRow === 0 && absDeltaCol === 1); // 1 case horizontalement

        if (!isValidOneStepMove) {
          console.log(
            `‚ùå ${player.heroType.toUpperCase()}: Doit se d√©placer exactement d'1 case dans une direction cardinale`
          );
          return false;
        }

        console.log(`‚úÖ ${player.heroType.toUpperCase()}: Mouvement valide`);
        return true;

      case "ninja":
        // Ninja: EXACTEMENT 2 cases dans UNE direction cardinale
        const isValidNinjaMove =
          (absDeltaRow === 2 && deltaCol === 0) || // 2 cases verticalement
          (deltaRow === 0 && absDeltaCol === 2); // 2 cases horizontalement

        if (!isValidNinjaMove) {
          console.log(
            "‚ùå NINJA: Doit se d√©placer EXACTEMENT de 2 cases dans une direction cardinale"
          );
          return false;
        }

        // V√©rifier que le chemin est libre pour le ninja
        if (!this.isPathClear(row, col, newRow, newCol)) {
          console.log("‚ùå NINJA: Chemin bloqu√©");
          return false;
        }

        // V√©rification suppl√©mentaire pour le Ninja: s'assurer qu'il n'y a pas de joueur sur le chemin
        const stepRow = deltaRow === 0 ? 0 : deltaRow > 0 ? 1 : -1;
        const stepCol = deltaCol === 0 ? 0 : deltaCol > 0 ? 1 : -1;
        
        let currentRow = row + stepRow;
        let currentCol = col + stepCol;
        
        // V√©rifier la case interm√©diaire
        if (this.grid[currentRow][currentCol].player) {
          console.log(`‚ùå NINJA: Joueur bloquant le chemin en (${currentRow}, ${currentCol})`);
          return false;
        }

        console.log("‚úÖ NINJA: Mouvement valide de 2 cases");
        return true;

      default:
        console.log(`‚ùå Type de h√©ros inconnu: ${player.heroType}`);
        return false;
    }
  }

  removePlayer(player) {
    const pos = player.position;
    if (pos && this.grid[pos.row] && this.grid[pos.row][pos.col]) {
      this.grid[pos.row][pos.col].player = null;
    }

    const index = this.players.indexOf(player);
    if (index > -1) {
      this.players.splice(index, 1);
    }
  }

  isValidPosition(row, col) {
    return row >= 0 && row < this.size && col >= 0 && col < this.size;
  }

  getAccessibleCells(player) {
    const accessible = [];
    const { row, col } = player.position;

    console.log(
      `üéØ Calcul des cases accessibles pour ${player.name} (${player.heroType}) √† la position (${row}, ${col})`
    );

    // Directions cardinales uniquement
    const directions = [
      { dr: -1, dc: 0, name: "haut" }, // Nord
      { dr: 1, dc: 0, name: "bas" }, // Sud
      { dr: 0, dc: -1, name: "gauche" }, // Ouest
      { dr: 0, dc: 1, name: "droite" }, // Est
    ];

    // D√©terminer la distance de d√©placement selon le type de h√©ros
    let distance;
    switch (player.heroType) {
      case "chevalier":
      case "sorcier":
        distance = 1; // Un seul pas pour le chevalier et le sorcier
        break;
      case "ninja":
        distance = 2; // Deux pas pour le ninja
        break;
      default:
        distance = 1;
    }

    directions.forEach((dir) => {
      const newRow = row + dir.dr * distance;
      const newCol = col + dir.dc * distance;

      console.log(
        `  üîç Test ${player.heroType} direction ${dir.name}: (${newRow}, ${newCol})`
      );

      if (
        this.isValidPosition(newRow, newCol) &&
        !this.grid[newRow][newCol].player
      ) {
        // Pour le ninja, v√©rifier que le chemin est libre
        if (distance === 2) {
          // V√©rifier la case interm√©diaire pour le Ninja
          const intermediateRow = row + dir.dr;
          const intermediateCol = col + dir.dc;
          
          if (!this.grid[intermediateRow][intermediateCol].player) {
            accessible.push({ row: newRow, col: newCol });
            console.log(
              `    ‚úÖ Case accessible pour ninja: (${newRow}, ${newCol})`
            );
          } else {
            console.log(
              `    ‚ùå Case non accessible pour ninja (case interm√©diaire bloqu√©e): (${intermediateRow}, ${intermediateCol})`
            );
          }
        } else {
          accessible.push({ row: newRow, col: newCol });
          console.log(
            `    ‚úÖ Case accessible pour ${player.heroType}: (${newRow}, ${newCol})`
          );
        }
      } else {
        console.log(
          `    ‚ùå Case non accessible pour ${player.heroType}: (${newRow}, ${newCol})`
        );
      }
    });

    console.log(
      `üéØ ${player.name} (${player.heroType}) peut acc√©der √† ${accessible.length} cases:`,
      accessible
    );
    return accessible;
  }

  // Nouvelle m√©thode pour v√©rifier si un chemin en L est libre
  isLPathClear(fromRow, fromCol, toRow, toCol) {
    // Calculer les deltas
    const deltaRow = toRow - fromRow;
    const deltaCol = toCol - fromCol;
    const absDeltaRow = Math.abs(deltaRow);
    const absDeltaCol = Math.abs(deltaCol);

    console.log(`üîç V√©rification chemin en L: de (${fromRow},${fromCol}) vers (${toRow},${toCol})`);

    // Cas 1: Mouvement horizontal puis vertical
    const horizontalFirst = {
      intermediate: { row: fromRow, col: fromCol + Math.sign(deltaCol) * 2 },
      final: { row: toRow, col: toCol }
    };

    // Cas 2: Mouvement vertical puis horizontal
    const verticalFirst = {
      intermediate: { row: fromRow + Math.sign(deltaRow) * 2, col: fromCol },
      final: { row: toRow, col: toCol }
    };

    // Tester les deux chemins possibles
    const paths = [horizontalFirst, verticalFirst];
    for (const path of paths) {
      const { intermediate, final } = path;
      
      // V√©rifier si la position interm√©diaire est valide
      if (!this.isValidPosition(intermediate.row, intermediate.col)) {
        console.log(`‚ùå Position interm√©diaire invalide: (${intermediate.row},${intermediate.col})`);
        continue;
      }

      // V√©rifier si la case interm√©diaire est libre
      if (this.grid[intermediate.row][intermediate.col].player) {
        console.log(`‚ùå Position interm√©diaire occup√©e: (${intermediate.row},${intermediate.col})`);
        continue;
      }

      // V√©rifier le chemin jusqu'√† la position interm√©diaire
      if (!this.isPathClear(fromRow, fromCol, intermediate.row, intermediate.col)) {
        console.log(`‚ùå Chemin vers position interm√©diaire bloqu√©`);
        continue;
      }

      // V√©rifier le chemin de la position interm√©diaire √† la position finale
      if (!this.isPathClear(intermediate.row, intermediate.col, final.row, final.col)) {
        console.log(`‚ùå Chemin de position interm√©diaire vers finale bloqu√©`);
        continue;
      }

      console.log(`‚úÖ Chemin en L valide trouv√© via (${intermediate.row},${intermediate.col})`);
      return { isValid: true, path: { intermediate, final } };
    }

    console.log("‚ùå Aucun chemin en L valide trouv√©");
    return { isValid: false, path: null };
  }

  // Nouvelle m√©thode pour v√©rifier si une position est atteignable par le Ninja
  isNinjaTargetReachable(fromRow, fromCol, toRow, toCol) {
    const deltaRow = Math.abs(toRow - fromRow);
    const deltaCol = Math.abs(toCol - fromCol);
    const totalDistance = deltaRow + deltaCol;

    console.log(`üîç V√©rification cible atteignable: de (${fromRow},${fromCol}) vers (${toRow},${toCol})`);

    // Cas 1: Cible adjacente (distance 1)
    if (totalDistance === 1 && (deltaRow === 0 || deltaCol === 0)) {
      console.log("‚úÖ Cible adjacente - Attaque de base");
      return true;
    }

    // Cas 2: Distance exacte de 3 cases (attaque sp√©ciale)
    if (totalDistance === 3) {
      // Cas 2.1: En ligne droite (3 cases dans une direction)
      if (deltaRow === 0 || deltaCol === 0) {
        const isReachable = this.isPathClear(fromRow, fromCol, toRow, toCol);
        console.log(`${isReachable ? "‚úÖ" : "‚ùå"} Attaque sp√©ciale en ligne droite`);
        return isReachable;
      }

      // Cas 2.2: En L (2+1 ou 1+2)
      if ((deltaRow === 2 && deltaCol === 1) || (deltaRow === 1 && deltaCol === 2)) {
        const pathCheck = this.isLPathClear(fromRow, fromCol, toRow, toCol);
        console.log(`${pathCheck.isValid ? "‚úÖ" : "‚ùå"} Attaque sp√©ciale en L`);
        return pathCheck.isValid;
      }
    }

    console.log("‚ùå Cible hors de port√©e - Le Ninja ne peut attaquer qu'√† distance 1 ou 3");
    return false;
  }

  // Nouvelle m√©thode pour calculer la position d'attaque du Ninja
  getNinjaAttackPosition(fromRow, fromCol, targetRow, targetCol) {
    const deltaRow = targetRow - fromRow;
    const deltaCol = targetCol - fromCol;
    const absDeltaRow = Math.abs(deltaRow);
    const absDeltaCol = Math.abs(deltaCol);

    console.log(`ü•∑ Calcul position d'attaque: de (${fromRow},${fromCol}) vers (${targetRow},${targetCol})`);

    // Si d√©j√† adjacent, pas besoin de se d√©placer
    if ((absDeltaRow === 1 && absDeltaCol === 0) || (absDeltaRow === 0 && absDeltaCol === 1)) {
      console.log("‚úÖ D√©j√† en position d'attaque");
      return { row: fromRow, col: fromCol };
    }

    // En ligne droite
    if (absDeltaRow === 0 || absDeltaCol === 0) {
      const moveRow = targetRow - Math.sign(deltaRow);
      const moveCol = targetCol - Math.sign(deltaCol);
      console.log(`‚úÖ Position d'attaque en ligne droite: (${moveRow},${moveCol})`);
      return { row: moveRow, col: moveCol };
    }

    // En L
    if ((absDeltaRow === 2 && absDeltaCol === 1) || (absDeltaRow === 1 && absDeltaCol === 2)) {
      // V√©rifier les deux chemins possibles
      const pathCheck = this.isLPathClear(fromRow, fromCol, targetRow, targetCol);
      
      if (pathCheck.isValid && pathCheck.path) {
        // Utiliser la position interm√©diaire comme position d'attaque
        const attackPos = pathCheck.path.intermediate;
        console.log(`‚úÖ Position d'attaque en L trouv√©e: (${attackPos.row},${attackPos.col})`);
        return attackPos;
      }
    }

    console.log("‚ùå Aucune position d'attaque valide trouv√©e");
    return null;
  }

  getAttackableTargets(player) {
    if (!player || !player.position) return [];

    const targets = [];
    const { row, col } = player.position;

    // Obtenir tous les joueurs vivants sauf le joueur actuel
    const potentialTargets = this.getAlivePlayers().filter((p) => p !== player);

    for (const target of potentialTargets) {
      if (!target.position) continue;

      const deltaRow = Math.abs(target.position.row - row);
      const deltaCol = Math.abs(target.position.col - col);
      const distance = deltaRow + deltaCol;
      const isDiagonal = deltaRow > 0 && deltaCol > 0;

      // V√©rifier si la cible est en ligne droite (non diagonale)
      const isInStraightLine = deltaRow === 0 || deltaCol === 0;

      let isValidTarget = false;

      switch (player.heroType) {
        case "chevalier":
          // Le chevalier ne peut attaquer qu'√† une case de distance, sans diagonale
          isValidTarget = distance === 1 && !isDiagonal;
          break;

        case "ninja":
          // Nouvelles r√®gles du Ninja
          isValidTarget = this.isNinjaTargetReachable(
            row, col,
            target.position.row,
            target.position.col
          );
          break;

        case "sorcier":
          // Le sorcier peut attaquer √† distance 2-3 en ligne droite
          if (!isDiagonal && isInStraightLine) {
            isValidTarget = distance >= player.minAttackRange && 
                          distance <= player.maxAttackRange;
          }
          break;
      }

      if (isValidTarget) {
        targets.push(target);
      }
    }

    return targets;
  }

  isPathClear(fromRow, fromCol, toRow, toCol) {
    const deltaRow = toRow - fromRow;
    const deltaCol = toCol - fromCol;

    // V√©rification suppl√©mentaire: pas de mouvement en diagonale
    if (deltaRow !== 0 && deltaCol !== 0) {
      console.log("‚ùå isPathClear: mouvement en diagonale d√©tect√©");
      return false;
    }

    const stepRow = deltaRow === 0 ? 0 : deltaRow > 0 ? 1 : -1;
    const stepCol = deltaCol === 0 ? 0 : deltaCol > 0 ? 1 : -1;

    let currentRow = fromRow + stepRow;
    let currentCol = fromCol + stepCol;

    // V√©rifier chaque case interm√©diaire (pas la destination finale)
    while (currentRow !== toRow || currentCol !== toCol) {
      if (this.grid[currentRow][currentCol].player) {
        console.log(
          `‚ùå Chemin bloqu√© √† la position (${currentRow}, ${currentCol})`
        );
        return false;
      }
      currentRow += stepRow;
      currentCol += stepCol;
    }

    return true;
  }

  getDistance(pos1, pos2) {
    return Math.abs(pos1.row - pos2.row) + Math.abs(pos1.col - pos2.col);
  }

  getAlivePlayers() {
    return this.players.filter((player) => player.isAlive());
  }

  getCellAt(row, col) {
    if (this.isValidPosition(row, col)) {
      return this.grid[row][col];
    }
    return null;
  }

  getPlusShapedZone(centerRow, centerCol, range) {
    const zone = [];
    
    // Add center cell
    zone.push({ row: centerRow, col: centerCol });
    
    // Add cells in each direction
    for (let i = 1; i <= range; i++) {
      // Up
      if (centerRow - i >= 0) {
        zone.push({ row: centerRow - i, col: centerCol });
      }
      // Down
      if (centerRow + i < this.size) {
        zone.push({ row: centerRow + i, col: centerCol });
      }
      // Left
      if (centerCol - i >= 0) {
        zone.push({ row: centerRow, col: centerCol - i });
      }
      // Right
      if (centerCol + i < this.size) {
        zone.push({ row: centerRow, col: centerCol + i });
      }
    }
    
    return zone;
  }

  getPlayersInZone(zone) {
    const players = [];
    zone.forEach(cell => {
      const cellContent = this.getCellAt(cell.row, cell.col);
      if (cellContent && cellContent.player && cellContent.player.isAlive()) {
        players.push(cellContent.player);
      }
    });
    return players;
  }
}

export { Arena };
